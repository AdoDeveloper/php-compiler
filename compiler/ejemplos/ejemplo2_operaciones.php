<?php
// Ejemplo 2: Operaciones aritméticas

$x = 15;
$y = 4;

// Operaciones básicas
$suma = $x + $y;
$resta = $x - $y;
$multiplicacion = $x * $y;
$division = $x / $y;

echo $suma;
echo $resta;
echo $multiplicacion;
echo $division;

// Operaciones con paréntesis
$resultado = ($x + $y) * 2;
echo $resultado;

// Incremento y decremento
$contador = 0;
$contador++;
echo $contador;

$contador--;
echo $contador;

// Operadores de asignación compuesta
$total = 100;
$total += 50;
echo $total;

$total -= 30;
echo $total;

?>
